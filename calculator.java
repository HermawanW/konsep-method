public class calculator {
    
    public int getPenjumlahan (int angka1, int angka2){
        int hasil_tambah = angka1 + angka2;
        return hasil_tambah;
    }
    
    public double getPengurangan (double angka1, double angka2){
        double hasil_kurang = angka1 - angka2;
        return hasil_kurang;
    }
    
    public int getPerkalian (int angka1, int angka2){
        int hasil_kali = angka1 * angka2;
        return hasil_kali;
    }
    
    public double getPembagian (double angka1, double angka2){
        double hasil_bagi = angka1 / angka2;
        return hasil_bagi;
    }
}
